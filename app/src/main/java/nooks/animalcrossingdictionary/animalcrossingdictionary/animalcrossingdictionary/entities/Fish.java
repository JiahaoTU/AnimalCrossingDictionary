package nooks.animalcrossingdictionary.animalcrossingdictionary.animalcrossingdictionary.entities;

public class Fish {

    private int id;
    private String name;
    private String name_fr;
    private String monthNorthern;
    private String monthSouthern;
    private String time;
    private String location;
    private String rarity;
    private String shadow;
    private int price;
    private String catchPhrase;
    private String museumPhrase;
    private String imageUri;
    private String iconUri;

    public Fish() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName_fr() {
        return name_fr;
    }

    public void setName_fr(String name_fr) {
        this.name_fr = name_fr;
    }

    public String getMonthNorthern() {
        return monthNorthern;
    }

    public void setMonthNorthern(String monthNorthern) {
        this.monthNorthern = monthNorthern;
    }

    public String getMonthSouthern() {
        return monthSouthern;
    }

    public void setMonthSouthern(String monthSouthern) {
        this.monthSouthern = monthSouthern;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getRarity() {
        return rarity;
    }

    public void setRarity(String rarity) {
        this.rarity = rarity;
    }

    public String getShadow() {
        return shadow;
    }

    public void setShadow(String shadow) {
        this.shadow = shadow;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getCatchPhrase() {
        return catchPhrase;
    }

    public void setCatchPhrase(String catchPhrase) {
        this.catchPhrase = catchPhrase;
    }

    public String getMuseumPhrase() {
        return museumPhrase;
    }

    public void setMuseumPhrase(String museumPhrase) {
        this.museumPhrase = museumPhrase;
    }

    public String getImageUri() {
        return imageUri;
    }

    public void setImageUri(String imageUri) {
        this.imageUri = imageUri;
    }

    public String getIconUri() {
        return iconUri;
    }

    public void setIconUri(String iconUri) {
        this.iconUri = iconUri;
    }
}
